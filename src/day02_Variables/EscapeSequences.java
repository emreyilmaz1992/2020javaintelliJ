package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Emre");
        System.out.println("\tEmre");
        System.out.println("\t\t\t\t\tEmre");
                // \t mean paragraph space //

        System.out.println("Cybertek\nSchool");
        System.out.println("\nMy\nName\nis\nEmre");
                // \n for the new line

        System.out.println("My favorite TV show is \"Game of Thrones\".");
        System.out.println("My Favorite book is \"Robinson Crouse\".");

        System.out.println("I live in Brooklyn\\NY");

        System.out.println("My favorite book is 'Java'");




    }
}
