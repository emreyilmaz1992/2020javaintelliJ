package day17_While_Loop;

public class While_Loop_Practise2 {
    public static void main(String[] args) {

        for (int i = 0; i <101 ; i++) {
            System.out.print("*");

        }


        int i = 1;
        while (i <101){
            System.out.println("*");
            i++;
        }

        int number = 3;

        System.out.println("***");
        while(number > 0){
            System.out.println("* *");
            number--;
        }
        System.out.println("***");


    }
}
