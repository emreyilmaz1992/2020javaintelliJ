package day10_Switch_Scanner;

public class SwitchStatement {
    public static void main(String[] args) {


        byte a = 4;

        float   a1 = 4.5f;
        long    a2 = 10l ;
        double  a3 = 2.5d;
        boolean a4 = true;  // those 4 valuables are not accepted in switch.

        switch (1){
            case 1:
                System.out.println("One");
                System.out.println("1234");
                break;

            case 2:
                System.out.println("Two");
                break;

            case 3:
                System.out.println("Three");
                break;

            case 4:
                System.out.println("Four");
                break;

            default:
                System.out.println("Invalid Case"); // doesn't matter where you place the default section.
                break;                              // default only gets executed if none of the case meet the requirements














        }







    }
}
