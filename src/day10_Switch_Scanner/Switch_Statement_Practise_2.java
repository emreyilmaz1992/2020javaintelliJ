package day10_Switch_Scanner;

public class Switch_Statement_Practise_2 {

    public static void main(String[] args) {

        int num =0;

        switch(2){
            case 1:
                    num+= 9;

            case 2:
                    num+= 2;


            case 3:
                    num+= 10;
                break;
            default:
                    num-= 5;
                    break;

        }
        System.out.println(num);









    }



}
